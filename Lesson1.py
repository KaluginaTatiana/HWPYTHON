# Task1 Напишите программу, которая принимает на вход цифру, 
# обозначающую день недели, и проверяет, является ли этот день выходным.

print('Введите номер дня недели: что понедельник - 1-й день недели ... воскресенье - 7-й день недели')
day = int(input())
if day > 7 or day < 1:
    print('Неверный ввод номера дня недели')
elif day == 6 or day == 7:
    print('Да, это выходной.')
else:
    print('Нет, это рабочий день')


# Task2 Напишите программу, которая принимает на вход координаты точки (X и Y), 
# причём X ≠ 0 и Y ≠ 0 и выдаёт номер четверти плоскости, в которой находится эта точка. 


print('Введите координату x:')
x = float(input())
print('Введите координату y:')
y = float(input())
if x == 0 or y == 0:
    print('Неверный ввод. X ≠ 0 и Y ≠ 0')
elif x > 0 and y > 0:
    print('1 четверть')
elif x > 0 and y < 0:
    print('2 четверть')
elif x < 0 and y < 0:
    print('3 четверть')
else:
    print('4 четверть')

