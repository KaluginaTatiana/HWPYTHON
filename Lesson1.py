# Task2.2 Напишите программу, которая принимает на вход координаты 
# двух точек и находит расстояние между ними в 2D пространстве.

print('Введите координату x1:')
x1 = float(input())
print('Введите координату y1:')
y1 = float(input())
print('Введите координату x2:')
x2 = float(input())
print('Введите координату y2:')
y2 = float(input())
dim = round(((x1 - x2) ** 2 + (y1 - y2) ** 2) ** (1/2), 2)
print(f'Расстояние между точками равно: {dim}')


